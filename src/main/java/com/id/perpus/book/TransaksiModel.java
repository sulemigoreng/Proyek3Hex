package com.id.perpus.book;

public class TransaksiModel {
	private String idPinjam;
	private String nim;
	private String nama;
	private String idBuku;
	private String judulBuku;
	private String tglPinjam;
	private String lamaPinjam;
	private String tglKembali;
	private String jatuhTempo;
	private String status;
	private String denda;
	
	
	
	public String getNim() {
		return nim;
	}
	public void setNim(String nim) {
		this.nim = nim;
	}
	public String getIdBuku() {
		return idBuku;
	}
	public void setIdBuku(String idBuku) {
		this.idBuku = idBuku;
	}
	public String getTglPinjam() {
		return tglPinjam;
	}
	public void setTglPinjam(String tglPinjam) {
		this.tglPinjam = tglPinjam;
	}
	public String getLamaPinjam() {
		return lamaPinjam;
	}
	public void setLamaPinjam(String lamaPinjam) {
		this.lamaPinjam = lamaPinjam;
	}
	public String getTglKembali() {
		return tglKembali;
	}
	public void setTglKembali(String tglKembali) {
		this.tglKembali = tglKembali;
	}
	public String getNama() {
		return nama;
	}
	public void setNama(String nama) {
		this.nama = nama;
	}
	public String getJudulBuku() {
		return judulBuku;
	}
	public void setJudulBuku(String judulBuku) {
		this.judulBuku = judulBuku;
	}
	public String getJatuhTempo() {
		return jatuhTempo;
	}
	public void setJatuhTempo(String jatuhTempo) {
		this.jatuhTempo = jatuhTempo;
	}
	public String getStatus() {
		return status;
	}
	public void setStatus(String status) {
		this.status = status;
	}
	public String getDenda() {
		return denda;
	}
	public void setDenda(String denda) {
		this.denda = denda;
	}
	public String getIdPinjam() {
		return idPinjam;
	}
	public void setIdPinjam(String idPinjam) {
		this.idPinjam = idPinjam;
	}
	
	
}
